# 13. Explain the concept of the Diamond Problem in multiple inheritance. How does Python resolve it?

class A:
    def greet(self):
        return "Hello from A"

class B(A):
    def greet(self):
        return "Hello from B"

class C(A):
    def greet(self):
        return "Hello from C"

class D(B, C):
    pass

d = D()
print(d.greet())  
print(D.__mro__)  
